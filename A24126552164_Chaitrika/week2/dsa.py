def make_examples(concept):
    examples = []
    name = concept["name"]
    for q_type, difficulty_templates in QUERY_TEMPLATES.items():
        for difficulty, q_tmpl in difficulty_templates.items():
            instruction = q_tmpl.format(name=name)
            r_tmpl = RESPONSE_TEMPLATES[q_type][difficulty]
            fmt = dict(concept)
            try:
                response = r_tmpl.format(**fmt)
            except KeyError as e:
                response = f"[Template key missing: {e}]"
            examples.append({
                "instruction": instruction,
                "response": response,
                "metadata": {
                    "concept": name,
                    "category": concept["category"],
                    "query_type": q_type,
                    "difficulty": difficulty
                }
            })
    return examples


